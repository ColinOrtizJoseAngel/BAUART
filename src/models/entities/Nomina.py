class Nomina:
    def __init__(self, id=None, id_empleado=None, fecha_inicio=None, fecha_final=None, extra=0, deuda=0, total_nomina=0, fecha_registro=None, observaciones=None):
        self.id = id
        self.id_empleado = id_empleado
        self.fecha_inicio = fecha_inicio
        self.fecha_final = fecha_final
        self.extra = extra
        self.deuda = deuda
        self.total_nomina = total_nomina
        self.fecha_registro = fecha_registro
        self.observaciones = observaciones  
